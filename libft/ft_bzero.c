#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*p;
	int				i;

	i = 0;
	p = (unsigned char *)s;
	while (n)
	{
		p[i] = '\0';
		i++;
		--n;
	}
}
